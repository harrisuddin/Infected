function Camera(map, width, height) {
    this.x = 0;
    this.y = 0;
    this.width = width;
    this.height = height;
    this.maxX = map.cols * map.tsize - width;
    this.maxY = map.rows * map.tsize - height;
}

//
//Camera movement speed in pixels per second.
//

Camera.SPEED = 256;

//
//Camera movement, determines new x and y coord values. (.prototype allows addition of new property)
//then "clamps" values (provides a boundary for each edge of the map).
//


Camera.prototype.move = function (delta, dirx, diry) {
    this.x += dirx * Camera.SPEED * delta;
    this.y += diry * Camera.SPEED * delta;

    this.x = Math.max(0, Math.min(this.x, this.maxX));
    this.y = Math.max(0, Math.min(this.y, this.maxY));
};

//
//Initiallises the game, loads in images, assigns tags.
//

Game.load = function () {
    return [
        ImageLoader.loadImage('tiles', '../assets/tiles.png'),
        ImageLoader.loadImage('infected', '../assets/PlayerI.PNG')
    ];
};

//
//Displays the game, checks for movement keys being pressed.
//

Game.display = function () {
    Keyboard.listenForEvents([Keyboard.MOVELEFT, Keyboard.MOVERIGHT, Keyboard.MOVEUP, Keyboard.MOVEDOWN]);
    this.tileAtlas = ImageLoader.getImage('tiles');
    this.camera = new Camera(map, 1024, 1024);
    this.infected = {x: 450, y: 400, image: ImageLoader.getImage('infected')};
};

//
//moves the camera with movement keys
//

Game.update = function (delta) {
    var dirx = 0;
    var diry = 0;
    if (Keyboard.isDown(Keyboard.MOVELEFT)) { dirx = -1; }
    if (Keyboard.isDown(Keyboard.MOVERIGHT)) { dirx = 1; }
    if (Keyboard.isDown(Keyboard.MOVEUP)) { diry = -1; }
    if (Keyboard.isDown(Keyboard.MOVEDOWN)) { diry = 1; }

    this.camera.move(delta, dirx, diry);
};

//
//loads the new part of the map coming onto the screen
//

Game._drawLayer = function (layer) {
    var startCol = Math.floor(this.camera.x / map.tsize);
    var endCol = startCol + (this.camera.width / map.tsize);
    var startRow = Math.floor(this.camera.y / map.tsize);
    var endRow = startRow + (this.camera.height / map.tsize);
    var offsetX = -this.camera.x + startCol * map.tsize;
    var offsetY = -this.camera.y + startRow * map.tsize;
    //iterates through the columns and rows to load in tiles
    for (var c = startCol; c <= endCol; c++) {
        for (var r = startRow; r <= endRow; r++) {
            var tile = map.getTile(layer, c, r);
            var x = (c - startCol) * map.tsize + offsetX;
            var y = (r - startRow) * map.tsize + offsetY;
            if (tile !== 0) { // 0 => empty tile
                this.ctx.drawImage(
                    this.tileAtlas, // image
                    (tile - 1) * map.tsize, // current x
                    0, // current y
                    map.tsize, // current width
                    map.tsize, // current height
                    Math.round(x),  // new x
                    Math.round(y), // new y
                    map.tsize, // new width
                    map.tsize // new height
                );
            }
        }
    }
};

//
//draw map background layer
// draw game sprites
// draw map top layer
//

Game.render = function () {
    this._drawLayer(0);
    this.ctx.drawImage(this.infected.image, this.infected.x, this.infected.y);
    this._drawLayer(1);
};
